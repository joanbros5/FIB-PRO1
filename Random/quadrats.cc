#include <iostream>
using namespace std;

int main() {
	int a, b, c;
	cout << "Write three numbers separated with a space to calculate their squares:" << endl;
	cin >> a >> b >> c;
	cout << a << "^2 = " << a * a << endl;
	cout << b << "^2 = " << b * b << endl;
	cout << c << "^2 = " << c * c << endl;
}
